# Description: Converts user given Celsius temperature to Fahrenheit temperature
print("Please enter Celsius temperature.")
Celsius = float(input())
Fahrenheit = float(((9 / 5) * Celsius) + 32)
print("The equivalent Fahrenheit temperature is:")
print(Fahrenheit)
